int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int		i[2];

	i[0] = 0;
	i[1] = n;
	while (s1[i[0]] || s2[i[0]])
	{
		if (i[0] > i[1])
			return (0);
		if (s1[i[0]] > s2[i[0]])
			return (1);
		if (s1[i[0]] < s2[i[0]])
			return (-1);
		i[0]++;
	}
	return (0);
}
