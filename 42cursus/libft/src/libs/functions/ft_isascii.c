#include "../libft.h"

int    ft_isascii(char *str)
{
    int     i;

    i = 0;
    while (str[i] && str[i] != '\0')
    {
        if (str[i] >= 0 && str[i] <= 127)
            return (1);
        i++;
    }
    return (0);
}