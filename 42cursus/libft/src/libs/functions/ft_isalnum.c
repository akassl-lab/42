#include "../libft.h"

int    ft_isalnum(char *str)
{
    int     i;

    i = 0;
    while (str[i] && str[i] != '\0')
    {
        if ((str[i] < 48 || str[i] > 57) && (str[i] < 65 || str[i] > 122))
            return (0);
        i++;
    }
    return (1);
}